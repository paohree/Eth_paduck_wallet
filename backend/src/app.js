/**
 * app.js — Express 애플리케이션 설정
 *
 * server.js와 app.js를 분리하는 이유:
 *   - app.js: 미들웨어, 라우터, 보안 설정 등 "앱 로직"
 *   - server.js: 포트 바인딩, 프로세스 시그널 처리 등 "서버 실행"
 *
 * 이 분리를 통해 테스트 환경에서 실제 포트를 열지 않고
 * app 객체만 import해서 테스트할 수 있다.
 */

"use strict";

const express = require("express");
const cors = require("cors");
const config = require("./config");
const balanceRouter = require("./routes/balance");

const app = express();

// ──────────────────────────────────────────────────────────
// 신뢰 프록시 설정
// ──────────────────────────────────────────────────────────
// 프록시(Nginx, AWS ELB 등) 뒤에 배포될 경우,
// req.ip가 프록시 IP가 아닌 실제 클라이언트 IP를 반환하도록 한다.
// X-Forwarded-For 헤더를 신뢰하게 된다.
//
// [보안 주의]
// 신뢰하지 않는 환경에서 이 설정을 켜면 클라이언트가 X-Forwarded-For를 조작해
// rate limiting을 우회할 수 있다. (예: X-Forwarded-For: 1.2.3.4 로 IP 스푸핑)
// 실제 배포 시에는 신뢰하는 프록시 수만큼만 지정해야 한다 (예: 1).
app.set("trust proxy", 1);

// ──────────────────────────────────────────────────────────
// JSON 파싱
// ──────────────────────────────────────────────────────────
// 이 API는 GET 요청만 받으므로 req.body 파싱이 당장 필요하지 않다.
// 하지만 Express 기본 설정이므로 포함한다.
app.use(express.json());

// ──────────────────────────────────────────────────────────
// CORS (Cross-Origin Resource Sharing)
// ──────────────────────────────────────────────────────────
// [왜 CORS가 필요한가?]
// 브라우저의 Same-Origin Policy:
//   프론트엔드(http://localhost:5173)에서 다른 출처(http://localhost:5000)로
//   fetch 요청을 보내면 브라우저가 기본으로 차단한다.
//   이는 브라우저의 보안 정책이며, curl이나 Postman은 영향을 받지 않는다.
//
// [왜 origin을 특정 주소로 제한하는가?]
// origin: "*" (와일드카드)는 어떤 출처에서든 이 API를 호출할 수 있게 한다.
// 이는 개발 편의를 위한 설정이지 운영에서는 피해야 한다.
//
// allowedOrigin을 환경변수로 관리하면:
//   - 개발: http://localhost:5173
//   - 배포: https://actual-domain.com
// 코드 변경 없이 환경에 맞는 설정을 적용할 수 있다.
app.use(
  cors({
    origin: config.allowedOrigin,
    methods: ["GET"],  // 이 API는 GET만 필요하다. 불필요한 메서드 허용을 차단한다.
    optionsSuccessStatus: 200,
  })
);

// ──────────────────────────────────────────────────────────
// 라우트 등록
// ──────────────────────────────────────────────────────────
app.use("/api/balance", balanceRouter);

// ──────────────────────────────────────────────────────────
// 헬스체크 엔드포인트
// ──────────────────────────────────────────────────────────
// 서버가 살아있는지 확인하는 간단한 엔드포인트.
// 로드밸런서나 모니터링 시스템이 주기적으로 호출한다.
// Alchemy를 호출하지 않으므로 비용이 없다.
app.get("/health", (req, res) => {
  res.json({ status: "ok", timestamp: new Date().toISOString() });
});

// ──────────────────────────────────────────────────────────
// 404 핸들러
// ──────────────────────────────────────────────────────────
// 등록된 라우트에 매칭되지 않는 모든 요청을 처리한다.
// Express 기본 동작(HTML 에러 페이지 반환)을 JSON으로 대체한다.
app.use((req, res) => {
  res.status(404).json({ error: "요청한 경로를 찾을 수 없습니다." });
});

// ──────────────────────────────────────────────────────────
// 전역 에러 핸들러
// ──────────────────────────────────────────────────────────
// next(err) 로 전달된 예상치 못한 에러를 처리한다.
// 에러 핸들러는 파라미터가 4개여야 Express가 에러 핸들러로 인식한다.
// eslint-disable-next-line no-unused-vars
app.use((err, req, res, next) => {
  // 상세 에러는 서버 로그에만 남긴다.
  console.error("[Global Error Handler]", err);

  // 클라이언트에는 일반 메시지만 반환한다. (시나리오 6: 정보 노출 방지)
  res.status(500).json({ error: "서버 내부 오류가 발생했습니다." });
});

module.exports = app;
